// Builds and renders the generated MeTTa API reference. The extractor reads the analyzer's indexed
// definitions, asks CoreRuntime for the same @doc-formal records hovers use, and attaches TypeScript host
// operations from HostTypeService. The JSON graph is the boundary; Markdown and VitePress sidebar files are
// derived from that graph.

import type { Range } from "vscode-languageserver-types";
