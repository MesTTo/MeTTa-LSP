import { createHash } from "node:crypto";
import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const BROWSER_IDE_WORKER_FILES = [
  "server/browserServer.js",
  "runtime/browserEvaluationWorker.js",
  "runtime/browserSemanticLintWorker.js",
  "runtime/browserHyperposeWorker.js",
];

export async function browserWorkerBuildId(outputRoot) {
  const hash = createHash("sha256");
  for (const relativePath of BROWSER_IDE_WORKER_FILES) {
    hash.update(relativePath);
    hash.update("\0");
    hash.update(await readFile(path.join(outputRoot, relativePath)));
    hash.update("\0");
  }
  return hash.digest("hex");
}

export async function writeBrowserWorkerVersion(outputRoot, generatedPath) {
  const buildId = await browserWorkerBuildId(outputRoot);
  const source = `// SPDX-FileCopyrightText: 2026 MesTTo
// SPDX-License-Identifier: Apache-2.0
// Generated by scripts/bundle-browser-ide.mjs. Do not edit manually.

export const BROWSER_WORKER_BUILD_ID = "${buildId}";
`;
  const previous = await readFile(generatedPath, "utf8").catch(() => "");
  if (previous !== source) await writeFile(generatedPath, source, "utf8");
  return buildId;
}
